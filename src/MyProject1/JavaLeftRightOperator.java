package MyProject1;

public class JavaLeftRightOperator {
	public static void main(String[] args) {
		int x = 20;
		System.out.println(x >> 2);// 20/2pow2
		System.out.println(x << 2);// 20*2pow2

		int y = 10;
		System.out.println(y << 4);// 10*2pow4
		int z = 1000;
		System.out.println(z >> 4);// 1000/2pow4
	}
}
